# 4^2014 = 2^4028
# 2^2015
# -8 = -2^3

# В двоичной записи:
# 2^4028 — это единица и 4028 нулей
# 2^2015 — единица и 2015 нулей
# Вычитание 2^3 даст цепочку единиц

# Более простое решение — аналитическое:
# Ответ: 4028 - 2015 + 4 = 2017? Проверим.

# Но для точности вычислим программно, но без гигантских строк (если степень очень большая)

# Так как 2014 не огромно, прямое вычисление работает:

exp = 4 ** 2014 + 2 ** 2015 - 8
binary = bin(exp)[2:]
ones = binary.count('1')
print(f"Единиц: {ones}")
