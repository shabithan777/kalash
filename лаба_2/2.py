def count_ones_manual():
    # Представляем выражение как: 2^4028 + 2^2015 - 2^3
    # Будем работать с массивом битов (длинная арифметика вручную)
    
    # Определим максимальную степень
    max_power = 4028
    # Создаём массив битов (0 - нет единицы, 1 - есть единица)
    bits = [0] * (max_power + 1)
    
    # Добавляем 2^4028
    bits[4028] += 1
    # Добавляем 2^2015
    bits[2015] += 1
    
    # Вычитаем 2^3
    # Вычитание: нужно занять старший бит
    bits[3] -= 1
    
    # Обработка заимствований при вычитании
    for i in range(len(bits)):
        if bits[i] < 0:
            # Занимаем у следующего разряда
            bits[i] += 2
            bits[i + 1] -= 1
    
    # Обработка переносов при сложении (если где-то стало 2)
    for i in range(len(bits)):
        if bits[i] >= 2:
            carry = bits[i] // 2
            bits[i] %= 2
            bits[i + 1] += carry
    
    # Подсчитываем количество единиц
    ones_count = sum(bits)
    
    return ones_count

print(count_ones_manual())