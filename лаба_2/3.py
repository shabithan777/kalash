def task3():
    # Список для хранения результатов
    result = []
    
    # m - четное число, начинаем с 0
    m = 0
    # Пока 2 в степени m не превышает верхнюю границу
    while 2 ** m <= 600_000_000:
        
        # n - нечетное число, начинаем с 1
        n = 1
        # Пока 3 в степени n не превышает верхнюю границу
        while 3 ** n <= 600_000_000:
            
            # Вычисляем N = 2^m * 3^n
            N = (2 ** m) * (3 ** n)
            
            # Проверяем, попадает ли N в отрезок [400_000_000; 600_000_000]
            if 400_000_000 <= N <= 600_000_000:
                result.append(N)  # Добавляем число в результат
            
            n += 2  # Увеличиваем на 2, чтобы n оставалось нечетным
        
        m += 2  # Увеличиваем на 2, чтобы m оставалось четным
    
    # Сортируем список по возрастанию
    result.sort()
    
    # Возвращаем результат
    return result

# Вызов функции и вывод каждого числа
for num in task3():
    print(num)