def split(lst, n):
    """
    Разбивает список lst на n подсписков,
    распределяя элементы поочерёдно.
    
    Пример:
    >>> split([1, 2, 3, 4, 5], 2)
    [[1, 3, 5], [2, 4]]
    
    >>> split([1, 2, 3, 4, 5], 3)
    [[1, 4], [2, 5], [3]]
    """
    # Создаём n пустых подсписков
    result = [[] for _ in range(n)]
    
    # Распределяем элементы по индексу подсписка = индекс элемента % n
    for i, value in enumerate(lst):
        result[i % n].append(value)
    
    return result


# Проверка
print(split([1, 2, 3, 4, 5], 2))  # [[1, 3, 5], [2, 4]]
print(split([1, 2, 3, 4, 5], 3))  # [[1, 4], [2, 5], [3]]
